import styles from './ErrorMessage.module.css'
import clsx from 'clsx'

/**
 * Accessible Error Message Component
 * - Uses role="alert" for immediate screen reader announcement
 * - Not indicated by color alone (includes icon)
 * - Can be used for form-level or field-level errors
 */
function ErrorMessage({ 
  message, 
  id,
  className,
  showIcon = true,
  variant = 'error' // error, warning, info, success
}) {
  if (!message) return null

  const icons = {
    error: (
      <svg
        width="20"
        height="20"
        viewBox="0 0 24 24"
        fill="none"
        stroke="currentColor"
        strokeWidth="2"
        aria-hidden="true"
      >
        <circle cx="12" cy="12" r="10" />
        <line x1="12" y1="8" x2="12" y2="12" />
        <line x1="12" y1="16" x2="12.01" y2="16" />
      </svg>
    ),
    warning: (
      <svg
        width="20"
        height="20"
        viewBox="0 0 24 24"
        fill="none"
        stroke="currentColor"
        strokeWidth="2"
        aria-hidden="true"
      >
        <path d="M10.29 3.86L1.82 18a2 2 0 0 0 1.71 3h16.94a2 2 0 0 0 1.71-3L13.71 3.86a2 2 0 0 0-3.42 0z" />
        <line x1="12" y1="9" x2="12" y2="13" />
        <line x1="12" y1="17" x2="12.01" y2="17" />
      </svg>
    ),
    info: (
      <svg
        width="20"
        height="20"
        viewBox="0 0 24 24"
        fill="none"
        stroke="currentColor"
        strokeWidth="2"
        aria-hidden="true"
      >
        <circle cx="12" cy="12" r="10" />
        <line x1="12" y1="16" x2="12" y2="12" />
        <line x1="12" y1="8" x2="12.01" y2="8" />
      </svg>
    ),
    success: (
      <svg
        width="20"
        height="20"
        viewBox="0 0 24 24"
        fill="none"
        stroke="currentColor"
        strokeWidth="2"
        aria-hidden="true"
      >
        <path d="M22 11.08V12a10 10 0 1 1-5.93-9.14" />
        <polyline points="22 4 12 14.01 9 11.01" />
      </svg>
    )
  }

  return (
    <div
      id={id}
      role="alert"
      aria-live="polite"
      className={clsx(styles.errorMessage, styles[variant], className)}
    >
      {showIcon && (
        <span className={styles.icon}>
          {icons[variant]}
        </span>
      )}
      <span className={styles.text}>{message}</span>
    </div>
  )
}

export default ErrorMessage